export function prepararSlotsEncantos(encantos, opcoes, quantidade) {
  const slots = Array.from({ length: quantidade }, (_item, index) => `slot${index + 1}`);

  const selecionados = new Set(
    slots
      .map(slot => encantos?.[slot])
      .filter(Boolean)
  );

  return slots.map((slot, index) => {
    const valorAtual = encantos?.[slot] ?? "";

    return {
      key: slot,
      label: `Encanto ${index + 1}`,
      value: valorAtual,
      options: opcoes.map(opcao => ({
        ...opcao,
        disabled:
          Boolean(opcao.value) &&
          opcao.value !== valorAtual &&
          selecionados.has(opcao.value)
      }))
    };
  });
}

